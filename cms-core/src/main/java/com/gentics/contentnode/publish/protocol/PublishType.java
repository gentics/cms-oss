package com.gentics.contentnode.publish.protocol;

/**
 * Enum representing the types of objects that can be published.
 */
public enum PublishType {
	/**
	 * Represents a page type.
	 */
	PAGE,

	/**
	 * Represents a form type.
	 */
	FORM,

	/**
	 * Represents any other type not specifically categorized.
	 */
	OTHER
}
