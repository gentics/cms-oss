package com.gentics.contentnode.rest.model;

/**
 * Possible modes for displaying construct edit actions.
 */
public enum EditorControlStyle {
	/** The controls will be placed left of the construct. */
	ASIDE,
	/** The controls will be placed above the construct. */
	ABOVE,
	/** The controls will not be shown, the tag editor is opened when the construct is clicked. */
	CLICK
}
